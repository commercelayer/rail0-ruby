#!/usr/bin/env ruby
# frozen_string_literal: true
#
# Code generation for the RAIL0 Ruby SDK.
#
# Generates lib/rail0/types.rb — documentation-only Struct definitions for the
# gateway's request/response schemas. The resource classes (lib/rail0/resources/)
# and the HTTP/signing layers are hand-maintained and are NOT generated: methods
# return plain Hashes (symbol keys), so these Structs are reference documentation,
# not runtime types. This mirrors the rail0-go generator, which likewise generates
# only its types file from the same schema.
#
# Schema source (in priority order):
#   1. RAIL0_SCHEMA_PATH env var — absolute path to a local openapi.json
#   2. Default: ../rail0-gateway/docs/openapi.json (sibling repo on the filesystem)
#
# Run from the repository root:
#   ruby gen/generate.rb

require "json"

# ---------------------------------------------------------------------------
# Schema path resolution
# ---------------------------------------------------------------------------

default_schema_path = File.expand_path("../../rail0-gateway/docs/openapi.json", __dir__)
schema_path = ENV.fetch("RAIL0_SCHEMA_PATH", default_schema_path)

unless File.exist?(schema_path)
  abort "ERROR: OpenAPI schema not found at #{schema_path}\n" \
        "Set RAIL0_SCHEMA_PATH to the correct path or place the schema at the default location."
end

schema  = JSON.parse(File.read(schema_path, encoding: "UTF-8"))
schemas = schema.dig("components", "schemas") || {}

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def ref_name(ref)
  ref.split("/").last
end

# A short Ruby-flavoured type hint for a property, derived from its OpenAPI type.
def type_comment(prop_def)
  if prop_def.key?("$ref")
    ref_name(prop_def["$ref"])
  elsif prop_def.key?("allOf")
    ref_entry = prop_def["allOf"].find { |e| e.key?("$ref") }
    ref_entry ? ref_name(ref_entry["$ref"]) : nil
  else
    case prop_def["type"]
    when "integer" then "Integer"
    when "number"  then "Numeric"
    when "boolean" then "Boolean"
    when "string"  then "String"
    when "array"   then "Array"
    when "object"  then "Hash"
    end
  end
end

def member_comment(prop_def, type_hint)
  parts = []
  parts << type_hint if type_hint
  desc = prop_def["description"]
  parts << desc if desc && !desc.empty?
  parts.empty? ? nil : parts.join(" — ")
end

# The gateway's generic "Error" schema maps to Rail0::Types::ApiErrorBody so it
# does not collide with Rail0::ApiError (the raised exception).
def struct_name(schema_name)
  schema_name == "Error" ? "ApiErrorBody" : schema_name
end

# ---------------------------------------------------------------------------
# Generate lib/rail0/types.rb
# ---------------------------------------------------------------------------

object_schemas = schemas.select { |_, v| v["type"] == "object" }

lines = []
lines << "# frozen_string_literal: true"
lines << "# Code generated by gen/generate.rb — DO NOT EDIT."
lines << "# Re-run `ruby gen/generate.rb` after updating the gateway OpenAPI schema."
lines << "#"
lines << "# Source: rail0-gateway/docs/openapi.json"
lines << "#"
lines << "# These Structs document the gateway's request/response shapes. They are"
lines << "# reference only — SDK methods return plain Hashes with symbol keys."
lines << ""
lines << "module Rail0"
lines << "  module Types"

object_schemas.each do |name, schema_def|
  ruby_name   = struct_name(name)
  description = schema_def["description"]
  properties  = schema_def["properties"] || {}

  lines << ""

  if description && !description.empty?
    current = "    # "
    description.split.each do |word|
      if current.length + word.length + 1 > 104
        lines << current.rstrip
        current = "    # #{word}"
      else
        current += (current == "    # " ? word : " #{word}")
      end
    end
    lines << current.rstrip
  end

  if properties.empty?
    lines << "    #{ruby_name} = Struct.new(keyword_init: true)"
    next
  end

  lines << "    #{ruby_name} = Struct.new("
  entries  = properties.map { |pn, pd| [pn, member_comment(pd, type_comment(pd))] }
  pad_width = entries.map { |pn, _| pn.length }.max + 3
  entries.each do |prop_name, inline_desc|
    padded  = ":#{prop_name},".ljust(pad_width)
    comment = inline_desc ? " # #{inline_desc}" : ""
    lines << "      #{padded}#{comment}".rstrip
  end
  lines << "      keyword_init: true"
  lines << "    )"
end

lines << "  end"
lines << "end"
lines << ""

types_path = File.expand_path("../lib/rail0/types.rb", __dir__)
File.write(types_path, lines.join("\n"))
puts "Generated #{types_path}"
puts "Schema:   #{schema_path}"
puts "Done."
