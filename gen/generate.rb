#!/usr/bin/env ruby
# frozen_string_literal: true
#
# Code generation pipeline for the RAIL0 Ruby SDK.
#
# Schema source (in priority order):
#   1. RAIL0_SCHEMA_PATH env var — absolute path to a local openapi.json
#   2. Default: ../rail0-api/doc/openapi.json (sibling repo on the local filesystem)
#
# Run from the repository root:
#   ruby gen/generate.rb

require 'json'

# ---------------------------------------------------------------------------
# Schema path resolution
# ---------------------------------------------------------------------------

default_schema_path = File.expand_path("../../rail0-api/doc/openapi.json", __dir__)
schema_path = ENV.fetch("RAIL0_SCHEMA_PATH", default_schema_path)

unless File.exist?(schema_path)
  abort "ERROR: OpenAPI schema not found at #{schema_path}\n" \
        "Set RAIL0_SCHEMA_PATH to the correct path or place the schema at the default location."
end

schema = JSON.parse(File.read(schema_path))
schemas = schema.dig("components", "schemas") || {}

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

PRIMITIVE_NAMES = %w[Address Bytes32 Uint256String].freeze

# Return true if the schema is a primitive alias (string with no object properties).
def primitive?(schema_def)
  schema_def["type"] == "string" && !schema_def.key?("properties")
end

# Resolve a $ref like "#/components/schemas/Address" to just the name.
def ref_name(ref)
  ref.split("/").last
end

# Determine the type annotation comment for a property.
def type_comment(prop_def)
  if prop_def.key?("$ref")
    ref_name(prop_def["$ref"])
  elsif prop_def.key?("allOf")
    all_of = prop_def["allOf"]
    ref_entry = all_of.find { |e| e.key?("$ref") }
    ref_entry ? ref_name(ref_entry["$ref"]) : nil
  elsif prop_def["type"] == "integer"
    "Integer"
  elsif prop_def["type"] == "boolean"
    "Boolean"
  elsif prop_def["type"] == "string"
    "String"
  elsif prop_def["type"] == "array"
    "Array"
  elsif prop_def["type"] == "object"
    "Hash"
  end
end

# Build the inline description comment for a member field.
def member_comment(prop_name, prop_def, type_hint)
  description = prop_def["description"]
  parts = []
  parts << type_hint if type_hint
  parts << description if description && !description.empty?
  parts.empty? ? nil : parts.join(" — ")
end

# Map schema name to Ruby Struct constant name (Error -> ApiErrorBody).
def struct_name(schema_name)
  schema_name == "Error" ? "ApiErrorBody" : schema_name
end

# ---------------------------------------------------------------------------
# Ordered list of object schemas to generate
# ---------------------------------------------------------------------------

# Core types first, then request/response types, alphabetical within groups.
STRUCT_ORDER = %w[
  PaymentConfig
  EIP712Domain
  EIP3009Message
  SigningPayload
  PaymentInput
  PaymentMethod
  CreatePaymentRequest
  CreatePaymentResponse
  PrepareTransactionResponse
  SubmitTransactionRequest
  ApproveRequest
  ApproveResponse
  CapturePaymentRequest
  CapturePaymentResponse
  ChargePaymentResponse
  PayerSignatureRequest
  PayerSignatureResponse
  AuthorizePaymentResponse
  ReleasePaymentResponse
  VoidPaymentResponse
  RefundPaymentRequest
  RefundPaymentResponse
  Error
].freeze

# Build ordered list: STRUCT_ORDER first, then any remaining object schemas.
object_schemas = schemas.select { |_, v| v["type"] == "object" }
ordered_names = (STRUCT_ORDER & object_schemas.keys) +
                (object_schemas.keys - STRUCT_ORDER)

# ---------------------------------------------------------------------------
# Code generation
# ---------------------------------------------------------------------------

lines = []
lines << "# frozen_string_literal: true"
lines << "# Code generated by gen/generate.rb — DO NOT EDIT."
lines << "# Re-run `ruby gen/generate.rb` after updating the schema in rail0-api."
lines << "#"
lines << "# Source: rail0-api/doc/openapi.json"
lines << ""
lines << "module Rail0"
lines << "  module Types"
lines << "    # Primitive type aliases (all are String at runtime)"
lines << "    # Address       = String  # Checksummed Ethereum address (42 chars, 0x-prefixed)"
lines << "    # Bytes32       = String  # 32-byte value, hex-encoded (66 chars, 0x-prefixed)"
lines << "    # Uint256String = String  # uint256 encoded as a decimal string"

ordered_names.each do |name|
  schema_def = schemas[name]
  next unless schema_def

  ruby_name   = struct_name(name)
  description = schema_def["description"]
  properties  = schema_def["properties"] || {}

  lines << ""

  # Doc comment from schema description.
  if description && !description.empty?
    # Wrap long descriptions at ~100 chars.
    words  = description.split
    current = "    # "
    words.each do |word|
      if current.length + word.length + 1 > 104
        lines << current.rstrip
        current = "    # #{word}"
      else
        current += (current == "    # " ? word : " #{word}")
      end
    end
    lines << current.rstrip
  end

  # Struct.new call.
  if properties.empty?
    lines << "    #{ruby_name} = Struct.new(keyword_init: true)"
  else
    lines << "    #{ruby_name} = Struct.new("

    prop_entries = properties.map do |prop_name, prop_def|
      type_hint   = type_comment(prop_def)
      inline_desc = member_comment(prop_name, prop_def, type_hint)
      [prop_name, inline_desc]
    end

    pad_width = prop_entries.map { |pn, _| pn.length }.max + 1
    prop_entries.each do |prop_name, inline_desc|
      sym     = ":#{prop_name},"
      padded  = sym.ljust(pad_width + 2)
      comment = inline_desc ? " # #{inline_desc}" : ""
      lines << "      #{padded}#{comment}".rstrip
    end

    lines << "      keyword_init: true"
    lines << "    )"
  end
end

lines << "  end"
lines << "end"
lines << ""

output = lines.join("\n")

out_path = File.expand_path("../lib/rail0/types.rb", __dir__)
File.write(out_path, output)
puts "Generated #{out_path}"
puts "Schema: #{schema_path}"
